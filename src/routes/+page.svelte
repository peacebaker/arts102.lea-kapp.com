    <p>However, these colors aren't so good to work with unless you plan on blinding someone, so you'll almost always want to desaturate them.  Since this requires some fancy-pants math, and no one likes math, I usually recommend using HSL notation.  HSL stands for Hue, Saturation, and Lightness (value), and you can set each of these values individually.</p>
    <p>There are 360 hues, which represent values around the RGB color wheel using angular degrees.  0/360 is red, 120 is green, and 240 is blue.  Both saturation and lightness can be set to values between 0 and 100%, and the browser does all the math for you.  Since 0% saturation is gray, 100% lightness is white, and 0% lightness is black, the color spectrum is slightly more limited, there are only 3,564,000 colors to choose from.</p>

